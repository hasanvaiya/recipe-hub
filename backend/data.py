def generate_full_500_recipes():
    cat_map = {c["id"]: c["name_bn"] for c in CATEGORIES}
    recipes = []

    for i, name in enumerate(RAW_NAMES):
        num = i + 1
        cat_id = CAT_IDS[Math_floor_idx(i)]
        cat_name = cat_map.get(cat_id, "বিশ্বমানের খাবার")
        vector_icon = get_dish_vector_icon(name, cat_id)
        
        ingredients, steps, tips, nutrition = generate_dish_details(name, num, cat_id)

        recipes.append({
            "id": f"recipe-{num}",
            "title_bn": f"{num}. {name}",
            "title_en": f"Recipe #{num} - {name}",
            "category_id": cat_id,
            "category_name_bn": cat_name,
            "vector_icon": vector_icon,
            "rating": round(4.6 + (num % 5) * 0.08, 1),
            "review_count": 120 + (num * 11) % 450,
            "prep_time_minutes": 15 + (num % 3) * 5,
            "cook_time_minutes": 20 + (num % 4) * 10,
            "total_time_minutes": 35 + (num % 4) * 10,
            "servings_default": 4,
            "difficulty": "সহজ" if num % 3 == 0 else ("মাঝারি" if num % 2 == 0 else "কঠিন"),
            "is_trending": (num % 5 == 0),
            "is_featured": (num % 10 == 0),
            "short_description": f"{name} — সম্পূর্ণ বাংলায় উপকরণের পরিমাপ ও ধাপে ধাপে তৈরির নিখুঁত রেসিপি গাইড।",
            "ingredients": ingredients,
            "instructions": steps,
            "nutrition": nutrition,
            "chef_tips": tips,
            "tags": [cat_id, "রেসিপি", name.split()[0]]
        })

    return recipes

def Math_floor_idx(i):
    return i // 50

ALL_RECIPES = generate_full_500_recipes()
